//Q2. Program to print 5 times "Marvellous" on Screen.

#include<stdio.h>

void Display()
{
    int iValue = 0;

    for(iValue = 1; iValue <= 5; iValue++)
    {
        printf("Marvellous\n");
    }
}

int main()
{
    Display();

    return 0;
}

/*
Output :
        Marvellous
        Marvellous
        Marvellous
        Marvellous
        Marvellous
*/
            
